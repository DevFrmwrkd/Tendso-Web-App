import nodemailer from 'nodemailer'
import {
    getApprovalEmailHtml,
    getPaymentConfirmationEmailHtml,
    getPaymentLinkEmailHtml,
    getPaymentFollowUpEmailHtml,
    getDomainLiveEmailHtml,
    getDomainSetupInProgressEmailHtml,
    getDomainRenewalReminderEmailHtml,
    getWithdrawalStatusEmailHtml,
} from './templates'

interface ApprovalEmailData {
    businessName: string
    businessOwnerName: string
    businessOwnerEmail: string
    websiteUrl: string
    amount: number
    submissionId: string
    paymentReference?: string // ND-XXXX-YYYY format, generated by paymentReferences.generate
}

export async function sendApprovalEmail(data: ApprovalEmailData) {
    const {
        businessName,
        businessOwnerName,
        businessOwnerEmail,
        websiteUrl,
        amount,
        submissionId
    } = data

    try {
        const transporter = nodemailer.createTransport({
            service: 'gmail',
            auth: {
                user: process.env.GMAIL_USER,
                pass: process.env.GMAIL_APP_PASSWORD
            }
        })

        const emailHtml = getApprovalEmailHtml({
            businessName,
            businessOwnerName,
            websiteUrl,
            amount,
            submissionId,
            paymentReference: data.paymentReference,
        })

        const info = await transporter.sendMail({
            from: `"Negosyo Digital" <${process.env.GMAIL_USER}>`,
            to: businessOwnerEmail,
            subject: `🎉 Your Website is Ready — ${businessName}`,
            html: emailHtml
        })

        return { success: true, messageId: info.messageId }
    } catch (error: any) {
        console.error('Error in sendApprovalEmail:', error)
        throw error
    }
}

